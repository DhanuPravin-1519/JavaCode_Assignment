//Write a program to print even & odd numbers from 0 to 100

package assignment_package;

public class Ab_39Assignment25 
{

	public static void main(String[] args) 
	{
		int i;
		for(i=1;i<=100;i++)
		{
			if (i%2==0)
			{
			System.out.println(i);
			}
		}
		
		
		for(i=1;i<=100;i++)
		{
			if (i%2==1)
			{
			System.out.println(i);
			}
		}

	}

}
