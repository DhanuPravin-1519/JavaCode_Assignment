//Declare and initialize each datatype
package packageone;

public class InitializationDeclaration {

	public static void main(String[] args) 
	{
		byte a= 127;
		System.out.println(a);
		short a1= 32767;
		System.out.println(a1);
		int a2=2147483647; // Declaration and initialization
		System.out.println(a2);
		long a3=922337203;
		System.out.println(a3);
		float a4= 10.89f;
		System.out.println(a4);
        double a5= 25.98;
        System.out.println(a5);
        char a6= 'a';
        System.out.println(a6);
        boolean a7=false;
        System.out.println(a7);
        String a8= "I am Dhruv";
        System.out.println(a8);
	}

}
